
public class Operacao {
	
	private char tipo;
	private char valor;
	
	public Operacao(char tipo, char valor) {
		super();
		this.tipo = tipo;
		this.valor = valor;
	}
	public char getTipo() {
		return tipo;
	}
	public void setTipo(char tipo) {
		this.tipo = tipo;
	}
	public char getValor() {
		return valor;
	}
	public void setValor(char valor) {
		this.valor = valor;
	}
	
	
	
}
